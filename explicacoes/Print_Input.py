# Aula de Print e Input em Python

# Parte 1: Usando print para exibir mensagens
print("Bem-vindo à aula de Python!")
print("Nesta aula, aprenderemos sobre o uso do print e input.")

# Parte 2: Usando input para obter dados do usuário
nome_usuario = input("Por favor, digite seu nome: ")

# Parte 3: Usando print para exibir uma saudação personalizada
print(f"Olá, {nome_usuario}! Seja bem-vindo(a) à nossa aula de Python.")

# Parte 4: Demonstração adicional do uso de print
print("Vamos explorar mais sobre como usar o print para exibir informações.")

# Parte 5: Exemplo de formatação usando print
idade = input("Digite sua idade: ")
print(f"Entendi, você tem {idade} anos.")

# Parte 6: Transformando a entrada do usuário em um número inteiro
idade = int(input("Digite sua idade: "))
print(f"Entendi, você tem {idade} anos.")

# Parte 7: Exemplo de formatação usando print
altura = float(input("Digite sua altura em metros: "))
print(f"Entendi, você tem {altura} metros de altura.")
